                    <h2>Portal do Conhecimento e CMPaaS</h2>

                    <p>Há vários anos o LIED (Laboratório de Informática na Educação), vinculado ao PPGI-UFES (Programa de Pós-Graduação em Informática da Universidade Federal do Espírito Santo), tem desenvolvido pesquisas relacionadas ao uso de Mapas Conceituais nos processos de Ensino e Aprendizagem. Tais pesquisas culminaram no desenvolvimento de soluções computacionais que facilitam, de alguma maneira, as operações de construção, análise e gestão de Mapas Conceituais.</p>

                    <p>No entanto, essas pesquisas careciam de um espaço integrado que permitisse seu acoplamento e integração com as demais, de modo a permitir um desenvolvimento gradual de uma plataforma de maior porte onde as funcionalidades desenvolvidas poderiam se complementar a fim de produzir informações mais ricas para as pesquisas e, além disto, compor um ambiente onde as pesquisas e ferramentas poderiam realmente ser utilizadas, testadas e validadas pela comunidade.</p>

                    <p>É este cenário que desejamos mudar através deste projeto. Ele é composto por dois subprojetos, quais sejam:</p>

                    <p>Plataforma CMPaaS: Todas as soluções computacionais têm suas funções desenvolvidas, implementadas e disponibilizadas. Estas soluções podem ser acessadas externamente ou internamente, permitindo integração entre os serviços oferecidos entre plataformas ou pela própria plataforma do CMPaaS.
                    <p>Portal do Conhecimento: Oferece uma interface de acesso amigável, permitindo aos usuários explorar as funcionalidades oferecidas pelo CMPaaS.</p>

                    <p>Desejamos que você aproveite ao máximo os serviços que oferecemos e colabore conosco fornecendo feedbacks e sugestões de melhorias para o aprimoramento constante das funcionalidades oferecidas na Plataforma CMPaaS e no Portal do Conhecimento.</p>
