package com.sample;

public class Animal {
	
	private int habitat;
	private int revestimento;
	private int locomocao;
	private int reproducao;
	private int alimentacao;
	private int respiracao;
	
	public int getHabitat() {
		return habitat;
	}
	public void setHabitat(int habitat) {
		this.habitat = habitat;
	}
	public int getRevestimento() {
		return revestimento;
	}
	public void setRevestimento(int revestimento) {
		this.revestimento = revestimento;
	}
	public int getLocomocao() {
		return locomocao;
	}
	public void setLocomocao(int locomocao) {
		this.locomocao = locomocao;
	}
	public int getReproducao() {
		return reproducao;
	}
	public void setReproducao(int reproducao) {
		this.reproducao = reproducao;
	}
	public int getAlimentacao() {
		return alimentacao;
	}
	public void setAlimentacao(int alimentacao) {
		this.alimentacao = alimentacao;
	}
	public int getRespiracao() {
		return respiracao;
	}
	public void setRespiracao(int respiracao) {
		this.respiracao = respiracao;
	}
	
	

}
